# 1. Даны целые числа а, b, с, являющиеся сторонами некоторого треугольника.
# Проверить истинность высказывания: «Треугольник со сторонами а, b, с является прямоугольным».

a, b, c = float(input("a: ")), float(input("b: ")), float(input("c: "))

if all([a > 0, b > 0, c > 0, a+b>c, a+c>b, b+c>a]):
    sides = sorted([a, b, c])
    is_right = abs(sides[0]**2 + sides[1]**2 - sides[2]**2) < 1e-9
    print("ИСТИНА" if is_right else "ЛОЖЬ")
else:
    print("Не треугольник")

