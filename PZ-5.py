# 2. Описать функцию SortDec3(А, В, С), меняющую содержимое переменных А, В, С таким образом,
# чтобы их значения оказались упорядоченными по убыванию (А, В, C вещественные параметры, являющиеся одновременно входными и выходными).
# С помощью этой функции упорядочить по убыванию два данных набора из трех чисел: (A1, В1, C1) и (А2, В2, С2).

def SortDec3(A, B, C):
    if A < B: A, B = B, A
    if A < C: A, C = C, A
    if B < C: B, C = C, B
    return A, B, C


