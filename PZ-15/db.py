def init_db(db_path: Path = DB_PATH):
    conn = sqlite3.connect(str(db_path))
    cursor = conn.cursor()

    cursor.execute('''
    create table if not exists Anketa (
        reg_num int primary key,
        last_name text,
        first_name text,
        middle_name text,
        birth_date text,
        awards text,
        address text,
        specialty text
    )
    ''')

    cursor.execute('select count(*) from Anketa')
    count = cursor.fetchone()[0]
    if count == 0:
        cursor.executemany('insert into Anketa values (?, ?, ?, ?, ?, ?, ?, ?)', applicants)
        conn.commit()

    return conn


